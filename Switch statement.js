const Morning = "11am";

switch (Morning) {
  case "8am":
    console.log("Wake up");
    break;
  case "9am":
    console.log("Breakfast");
    break;
  case "10am":
    console.log("Get ready for the centre");
    break;
  case "11am":
    console.log("Go to the centre");
    break;
  case "12am":
    console.log("Coding, coding & coding!!!");
}
